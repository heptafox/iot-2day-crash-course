# Boolean Logic - Example

# Simple conditions
sunny = True
cold = False

print("sunny =", sunny)
print("cold =", cold)
print()

# AND - both must be True
print("AND operator:")
print("sunny and cold:", sunny and cold)
print("sunny and not cold:", sunny and (not cold))
print()

# OR - at least one must be True
print("OR operator:")
print("sunny or cold:", sunny or cold)
print()

# NOT - reverses True/False
print("NOT operator:")
print("not sunny:", not sunny)
print("not cold:", not cold)
