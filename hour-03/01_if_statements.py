# Hour 03: If Statements
# The if statement runs code only when something is true

# Example 1: Check if someone is tall
height = 180
if height > 170:
    print("You are tall!")

# Example 2: Check if it's sunny
sunny = True
if sunny:
    print("Let's go outside!")

# Example 3: Check a number
number = 10
if number > 5:
    print("This number is big!")
