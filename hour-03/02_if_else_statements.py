# Hour 03: If-Else Statements
# If-else gives two choices: do this OR do that

# Example 1: Hot or cold
temperature = 15
if temperature > 20:
    print("It's warm!")
else:
    print("It's cold!")

# Example 2: Big or small number
number = 3
if number > 5:
    print("Big number")
else:
    print("Small number")

# Example 3: Happy or sad
happy = False
if happy:
    print("I'm smiling!")
else:
    print("I'm sad")