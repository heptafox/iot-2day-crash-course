# Hour 03: Simple Practice
# Easy examples to try

# Example 1: Am I hungry?
hungry = True
if hungry:
    print("Let's eat!")
else:
    print("I'm full!")

# Example 2: What's my favorite color?
color = "blue"
if color == "red":
    print("I like red!")
elif color == "blue":
    print("I like blue!")
else:
    print("I like other colors!")

# Example 3: Is it a big number?
my_number = 7
if my_number > 10:
    print("That's a big number!")
elif my_number > 5:
    print("That's a medium number!")
else:
    print("That's a small number!")