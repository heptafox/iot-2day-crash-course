# Hour 06 - Math Library (Simple Version)
# The math library has useful math functions

print("MATH LIBRARY - Easy Examples")
print("=" * 32)

# Import the math library
import math

# Example 1: Square root
print("\n1. Square root:")
print("Square root of 16 is", math.sqrt(16))
print("Square root of 25 is", math.sqrt(25))
print("Square root of 9 is", math.sqrt(9))

# Example 2: Rounding numbers
print("\n2. Rounding numbers:")
print("Round up 4.2:", math.ceil(4.2))
print("Round up 4.8:", math.ceil(4.8))
print("Round down 4.2:", math.floor(4.2))
print("Round down 4.8:", math.floor(4.8))

# Example 3: Power (like multiplication)
print("\n3. Power (x to the power of y):")
print("2 to the power of 3:", math.pow(2, 3))
print("5 to the power of 2:", math.pow(5, 2))
print("3 to the power of 4:", math.pow(3, 4))

# Example 4: Pi number
print("\n4. The number Pi:")
print("Pi is approximately", math.pi)
print("Area of circle with radius 5:", math.pi * 5 * 5)

# Example 5: Absolute value (always positive)
print("\n5. Absolute value (remove minus sign):")
print("Absolute value of -10:", math.fabs(-10))
print("Absolute value of -3.5:", math.fabs(-3.5))
print("Absolute value of 7:", math.fabs(7))

print("\nThe math library makes math easy!")